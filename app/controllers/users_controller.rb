class UsersController < ApplicationController
  def index
    @users = User.all
  end

  def show
    @user = User.find_by_slug(params[:user_slug])
  end

  def edit
    @user = User.find(params[:id])
  end

  def update
    @user = User.find(params[:id])

    if @user.update_attributes(params[:user])
      redirect_to "/#{@user.slug}", notice: 'User was successfully updated.'
    else
      render action: "edit"
    end
  end

  def destroy
    @user = User.find(params[:id])
    @user.destroy

    redirect_to users_url
  end
end
